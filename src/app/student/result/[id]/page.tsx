import { createClient } from '@/lib/supabase/server'
import { redirect } from 'next/navigation'
import Link from 'next/link'
import DownloadButtonClient from './DownloadButtonClient'

export default async function StudentResultPage({ params }: { params: Promise<{ id: string }> }) {
    const { id } = await params
    const supabase = await createClient()

    // 1. Authenticate Request & User
    const { data: { user } } = await supabase.auth.getUser()
    if (!user) redirect('/login')

    // 2. Fetch Consulting Request data
    const { data: request, error: reqError } = await supabase
        .from('consulting_requests')
        .select('*, users ( full_name )')
        .eq('id', id)
        .single()

    if (reqError || !request || request.student_id !== user.id) {
        redirect('/student/dashboard') // Ensure they own this request
    }

    // 3. Fetch specific Results generated by Consultant
    const { data: result } = await supabase
        .from('results')
        .select('final_content, created_at')
        .eq('request_id', id)
        .single()

    if (request.status !== 'completed' || !result) {
        return (
            <div className="min-h-screen p-8 bg-gray-50 flex flex-col items-center justify-center">
                <h1 className="text-2xl font-bold mb-4">Report Not Unlocked</h1>
                <p className="text-gray-500 mb-8">This request has not been completely processed yet.</p>
                <Link href="/student/dashboard" className="text-blue-600 hover:underline">Return to Dashboard</Link>
            </div>
        )
    }

    const studentName = request.users?.full_name || 'Student'
    const dateStr = new Date(result.created_at).toLocaleDateString()

    // Ensure line-breaks render properly in the standard HTML block
    const formattedHtmlContent = result.final_content.split('\n').map((line: string, i: number) => (
        <span key={i}>
            {line}
            <br />
        </span>
    ))

    return (
        <div className="min-h-screen p-8 bg-gray-50 dark:bg-zinc-950 font-sans">
            <div className="max-w-4xl mx-auto space-y-6">

                <header className="flex items-center justify-between border-b border-gray-200 dark:border-zinc-800 pb-4">
                    <div>
                        <h1 className="text-3xl font-bold dark:text-gray-100 flex items-center gap-2">
                            Consulting Result
                        </h1>
                        <p className="text-gray-500 text-sm mt-1">{request.target_company}</p>
                    </div>
                    <Link href="/student/dashboard" className="text-sm font-medium text-blue-600 hover:text-blue-500 transition-colors">
                        &larr; Back to Dashboard
                    </Link>
                </header>

                <main className="grid grid-cols-1 md:grid-cols-3 gap-6">

                    {/* HTML Display Panel */}
                    <section className="md:col-span-2 bg-white dark:bg-zinc-900 border border-gray-200 dark:border-zinc-800 rounded-xl p-8 shadow-sm h-[600px] overflow-y-auto">
                        <h2 className="text-xl font-semibold mb-6 dark:text-gray-200">Consultant Feedback</h2>
                        <div className="prose prose-sm md:prose-base dark:prose-invert max-w-none text-gray-800 dark:text-gray-200 leading-relaxed">
                            {formattedHtmlContent}
                        </div>
                    </section>

                    {/* Sidebar with Meta Data & Download Button */}
                    <aside className="space-y-6">
                        <div className="bg-white dark:bg-zinc-900 border border-gray-200 dark:border-zinc-800 rounded-xl p-6 shadow-sm">
                            <h3 className="text-sm font-semibold text-gray-500 uppercase tracking-wider mb-4">Report Details</h3>

                            <dl className="space-y-4 text-sm">
                                <div>
                                    <dt className="text-gray-500 dark:text-gray-400">Date Generated</dt>
                                    <dd className="font-medium text-gray-900 dark:text-gray-100 mt-1">{dateStr}</dd>
                                </div>
                                <div>
                                    <dt className="text-gray-500 dark:text-gray-400">Target Role</dt>
                                    <dd className="font-medium text-gray-900 dark:text-gray-100 mt-1">{request.target_company}</dd>
                                </div>
                                <div>
                                    <dt className="text-gray-500 dark:text-gray-400">Status</dt>
                                    <dd className="inline-flex mt-1 items-center px-2.5 py-0.5 rounded-full text-xs font-medium bg-emerald-100 text-emerald-800 dark:bg-emerald-900/20 dark:text-emerald-300">
                                        Completed Verified
                                    </dd>
                                </div>
                            </dl>
                        </div>

                        <div className="bg-indigo-50 dark:bg-indigo-950/20 border border-indigo-100 dark:border-indigo-900/40 rounded-xl p-6 shadow-sm">
                            <h3 className="text-lg font-bold text-indigo-900 dark:text-indigo-100 mb-2">Export Document</h3>
                            <p className="text-sm text-indigo-700 dark:text-indigo-300 mb-6 leading-relaxed">
                                Download this comprehensive guide as a professionally formatted PDF for your records.
                            </p>

                            <DownloadButtonClient
                                studentName={studentName}
                                targetCompany={request.target_company}
                                finalContent={result.final_content}
                                date={dateStr}
                            />
                        </div>
                    </aside>
                </main>

            </div>
        </div>
    )
}
